;;; USER Code

;;;  These constants define the dimensions of the square
ROW1 .CONST 40
ROW2 .CONST 100
COL1 .CONST 30
COL2 .CONST 120

;;;  Define some colors
	
RED   .UCONST x7C00	; 0 11111 00000 00000
GREEN .UCONST x03E0	; 0 00000 11111 00000
BLUE  .UCONST x001F	; 0 00000 00000 11111
WHITE .UCONST x7FFF	; 0 11111 11111 11111
	
	.CODE
	.ADDR x0000
;;; Draw a square 

	LC R0, ROW1
	LC R1, COL1
	LC R2, COL2
	LC R3, RED

	TRAP x02		; DRAW H LINE

	
	LC R0, ROW2
	LC R1, COL2
	LC R2, COL1
	LC R3, GREEN

	TRAP x02		; DRAW H LINE

	
	LC R0, COL1
	LC R1, ROW1
	LC R2, ROW2
	LC R3, BLUE

	TRAP x03		; DRAW V LINE
	
	LC R0, COL2
	LC R1, ROW2
	LC R2, ROW1
	LC R3, WHITE

	TRAP x03		; DRAW V LINE

END_PROGRAM